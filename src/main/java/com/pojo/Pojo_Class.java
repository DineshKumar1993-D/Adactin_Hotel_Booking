package com.pojo;

public class Pojo_Class {

	// Encapsulation--> Binding of code and data
	// together in single entity

	// 1. Declare the element as private

	// 2. Generate Getters and setters

	// 3. Initialize the element

	private String name;

	private int age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
